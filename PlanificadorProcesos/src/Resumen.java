
public class Resumen {

	private char PID;
	private int TS;
	private float TR;
	private float TE;
	
	public char getPID() {
		return PID;
	}
	public void setPID(char pID) {
		PID = pID;
	}
	public int getTS() {
		return TS;
	}
	public void setTS(int tS) {
		TS = tS;
	}
	public float getTR() {
		return TR;
	}
	public void setTR(float tR) {
		TR = tR;
	}
	public float getTE() {
		return TE;
	}
	public void setTE(float tE) {
		TE = tE;
	}
	
	
	
}
